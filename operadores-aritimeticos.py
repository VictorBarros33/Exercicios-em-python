# + é o sinal de adição
# - é o sinal de subtração
# * é o sinal de multiplicação
# ** é o sinal dee potencia
# / é o sinal de divisão
# // é o sinal de divisão inteira
# % é o sinal de resto da divisão
# == é o sinal de igualdade

n = int(input ('Digite um número: '))
n1= n+1
n2= n-1
n3= n*2
n4= n*3
n5= n**0.5
print ('o valor do sucessor é{0}, do antecessor é {1}, dobro{2}, triplo{3}, raiz{4}'.format(n1, n2, n3, n4, n5))